# Integers
x = 10
y = 20
sum = x + y
print("Sum of x and y:", sum)

# Floats
a = 2.5
b = 1.5
product = a * b
print("Product of a and b:", product)

# Strings
name = "John"
greeting = "Hello, " + name + "!"
print(greeting)

# Booleans
is_adult = True
is_student = False
print("Is adult:", is_adult)
print("Is student:", is_student)
