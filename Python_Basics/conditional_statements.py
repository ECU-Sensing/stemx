# If statement
x = 10
if x > 0:
    print("x is positive")

# If-else statement
y = -5
if y > 0:
    print("y is positive")
else:
    print("y is not positive")

# If-elif-else statement
z = 0
if z > 0:
    print("z is positive")
elif z < 0:
    print("z is negative")
else:
    print("z is zero")
