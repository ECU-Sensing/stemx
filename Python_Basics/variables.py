# Assigning values to variables
name = "John"
age = 25
is_student = True

# Printing out the variables
print("Name:", name)
print("Age:", age)
print("Is Student:", is_student)

# Modifying the variables
name = "Jane"
age += 1
is_student = False

# Printing out the modified variables
print("Name:", name)
print("Age:", age)
print("Is Student:", is_student)